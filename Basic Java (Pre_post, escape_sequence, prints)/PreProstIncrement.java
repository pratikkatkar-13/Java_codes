public class PreProstIncrement {

    public static void main(String[] args) {

        //post increment
        int a = 10;
        System.out.println(a++);
        System.out.println(a);

        //post decrement
        int b = 10;
        System.out.println(b--);
        System.out.println(b);


        //pre increment
        int c = 10;
        System.out.println(++c);
        System.out.println(c);


        //pre decrement
        int d =10;
        System.out.println(--d);
        System.out.println(d);
    }
}